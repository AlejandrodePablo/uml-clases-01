public class MemoriaRam {
    public String marca;
    public String modelo;
    public Double precio;
    public Integer almacenamiento;
    public Integer frecuencia;
    public Integer modulosMemoria;

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public Double getPrecio() {
        return precio;
    }

    public void setPrecio(Double precio) {
        this.precio = precio;
    }

    public Integer getAlmacenamiento() {
        return almacenamiento;
    }

    public void setAlmacenamiento(Integer almacenamiento) {
        this.almacenamiento = almacenamiento;
    }

    public Integer getFrecuencia() {
        return frecuencia;
    }

    public void setFrecuencia(Integer frecuencia) {
        this.frecuencia = frecuencia;
    }

    public Integer getModulosMemoria() {
        return modulosMemoria;
    }

    public void setModulosMemoria(Integer modulosMemoria) {
        this.modulosMemoria = modulosMemoria;
    }
}

